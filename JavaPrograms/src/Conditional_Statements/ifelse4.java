package Conditional_Statements;

public class ifelse4 {

	public static void main(String[] args) {
		
		int a = 50;
		int b = 59;
		
		if (a==b) {
			System.out.println("equal");	
		}
		else if (a>b) {
			System.out.println("greater");
		}
		else {
			System.out.println("lesser");
		}
		}
	}

